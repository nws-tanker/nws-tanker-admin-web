const STAGE_BY_TAB: Record<InspectionTab, string> = {
  'pending-review': 'in_review',
  'pending-inspection': 'pending',
  submitted: 'submitted',
  'lab-testing': 'lab_pending',
  approved: 'approved',
  rejected: 'rejected',
};

export function buildMockInspectionResponse(
  tab: InspectionTab,
  search: string,
  page: number,
  size: number,
): ApiInspectionPagedData {
  const counts = {
    in_review: MOCK_INSPECTION_RECORDS.filter(
      (r) => r.prior_stage === 'in_review',
    ).length,
    pending: MOCK_INSPECTION_RECORDS.filter((r) => r.prior_stage === 'pending')
      .length,
    submitted: MOCK_INSPECTION_RECORDS.filter(
      (r) => r.prior_stage === 'submitted',
    ).length,
    lab_pending: MOCK_INSPECTION_RECORDS.filter(
      (r) => r.prior_stage === 'lab_pending',
    ).length,
    approved: MOCK_INSPECTION_RECORDS.filter(
      (r) => r.prior_stage === 'approved',
    ).length,
    rejected: MOCK_INSPECTION_RECORDS.filter(
      (r) => r.prior_stage === 'rejected',
    ).length,
    cancelled: 0,
  };

  let records = MOCK_INSPECTION_RECORDS.filter(
    (r) => r.prior_stage === STAGE_BY_TAB[tab],
  );

  if (search) {
    const q = search.toLowerCase();
    records = records.filter(
      (r) =>
        r.id.toLowerCase().includes(q) ||
        r.plate.toLowerCase().includes(q) ||
        r.inspector_name.toLowerCase().includes(q),
    );
  }

  const totalElements = records.length;
  const totalPages = Math.max(1, Math.ceil(totalElements / size));
  const start = page * size;

  return {
    counts,
    data: records.slice(start, start + size),
    totalElements,
    totalPages,
    page,
    size,
  };
}
